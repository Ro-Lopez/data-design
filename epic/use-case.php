			<h2>User Story:</h2> As a user, I want to favorite a product.
			<br>

			<h2>Use-Case:</h2> <p>Persona: Kirt Sanders is a small business owner that checks a competitors website a the very beginning of the start of the work week (9:00 AM Monday). He likes to be able to login and see all the products he has or will be favoriting to price match the products that he sells. He has to be able to go back to weeks, months, even years to see a product at a certain time.</p>

			<p>He wants to be able to navigate the website easily and efficiently to bring up a list of all products that he has favorited on his competitors website. He then can check all the pricing his competitor has and base his pricing off that. Due to the nature of "time is money" and he has average IT skills, Kirt wants his list quickly and updated frequently.</p>



			<p><strong>Interaction Flow</strong></p>
			<ol>
				<li>User enters website - system directs them to their page</li>
				<li>User clicks log in - system bring up a pop for user to enter his information</li>
				<li>User enter all information required and clicks ok - system verifies information and closes pop up window</li>
				<li>User searches for product - system directs user to best search matches</li>
				<li>User hits favorite on one or mutltiple products to price match - system stores information for future use</li>
				<li>User clicks view all favorited items - system directs user to a list of all those products</li>
			</ol>

